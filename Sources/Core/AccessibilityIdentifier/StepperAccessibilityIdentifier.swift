/// The accessibility identifiers for the stepper.
public enum StepperAccessibilityIdentifier {
    /// The accessibility identifiers for the decrement stepper button.
    ///
    /// The value is equals to **spark-stepper-decrement-button**.
    public static let decrementButton = "spark-stepper-decrement-button"
    /// The accessibility identifiers for the increment stepper button.
    ///
    /// The value is equals to **spark-stepper-increment-button**.
    public static let incrementButton = "spark-stepper-increment-button"
}
